// ── Types ──────────────────────────────────────────────────────────────────────

export type ExampleDomain =
  | 'mechanical'
  | 'electrical'
  | 'thermal'
  | 'fluid'
  | 'control'
  | 'ml'
  | 'optimisation'
  | 'vehicle'
  | 'signal'
  | 'math'
  | 'data'
  | 'finance'

export interface ExampleGraph {
  id: string
  title: string
  domain: ExampleDomain
  description: string
  tags: string[]
  difficulty: 'beginner' | 'intermediate' | 'advanced'
  /** EngineSnapshotV1-compatible graph data */
  graph: {
    version: 1
    nodes: Array<{ id: string; blockType: string; position?: { x: number; y: number }; data: Record<string, unknown> }>
    edges: Array<{ id: string; source: string; sourceHandle: string; target: string; targetHandle: string }>
  }
}

// ── Helper to build simple graphs ─────────────────────────────────────────────

function n(id: string, blockType: string, data: Record<string, unknown> = {}, x = 0, y = 0) {
  return { id, blockType, position: { x, y }, data: { blockType, ...data } }
}

function e(id: string, src: string, srcH: string, tgt: string, tgtH: string) {
  return { id, source: src, sourceHandle: srcH, target: tgt, targetHandle: tgtH }
}

function g(nodes: ReturnType<typeof n>[], edges: ReturnType<typeof e>[]): ExampleGraph['graph'] {
  return { version: 1, nodes, edges }
}

