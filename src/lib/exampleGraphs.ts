// ── Query helpers ──────────────────────────────────────────────────────────────

/** Return all examples for a given domain. */
export function getExamplesByDomain(domain: ExampleDomain): ExampleGraph[] {
  return EXAMPLE_GRAPH_CATALOG[domain] ?? []
}

/** Simple case-insensitive keyword search across title, description, and tags. */
export function searchExamples(query: string): ExampleGraph[] {
  if (!query.trim()) return EXAMPLE_GRAPHS
  const q = query.toLowerCase()
  return EXAMPLE_GRAPHS.filter(
    (ex) =>
      ex.title.toLowerCase().includes(q) ||
      ex.description.toLowerCase().includes(q) ||
      ex.tags.some((t) => t.toLowerCase().includes(q)) ||
      ex.domain.toLowerCase().includes(q),
  )
}

/** Return examples by difficulty. */
export function getExamplesByDifficulty(
  level: ExampleGraph['difficulty'],
): ExampleGraph[] {
  return EXAMPLE_GRAPHS.filter((ex) => ex.difficulty === level)
}
