/**
 * gen-rust-licences.mjs — 16.1: Generate Rust dependency licence report.
 *
 * Reads cargo metadata and outputs:
 *  - legal/rust-dependency-licences.md (human-readable table)
 *
 * Run: npm run licence:rust
 * Regenerate after any Cargo.lock change.
 */

import { execSync } from 'node:child_process'
import { writeFileSync, mkdirSync } from 'node:fs'
import { join } from 'node:path'

const ROOT = new URL('..', import.meta.url).pathname.replace(/^\/([A-Z]:)/, '$1')

// Fetch metadata
const metaJson = execSync('cargo metadata --format-version 1', {
  cwd: ROOT,
  maxBuffer: 50 * 1024 * 1024,
}).toString()
const data = JSON.parse(metaJson)

// Workspace crate names to exclude
const workspace = new Set(data.workspace_members.map((m) => m.split(' ')[0]))

const seen = new Set()
const deps = []
for (const p of data.packages) {
  const key = `${p.name}@${p.version}`
  if (seen.has(key)) continue
  seen.add(key)
  if (workspace.has(p.name)) continue
  deps.push({
    name: p.name,
    version: p.version,
    license: p.license || 'UNKNOWN',
    repository: p.repository || '',
  })
}
deps.sort((a, b) => a.name.localeCompare(b.name))

// Check for disallowed licences
const DENY = ['GPL-2.0', 'GPL-3.0', 'AGPL-3.0', 'SSPL-1.0']
const violations = deps.filter((d) => DENY.some((bad) => d.license.includes(bad)))
if (violations.length > 0) {
  console.error('LICENCE VIOLATIONS:')
  for (const v of violations) {
    console.error(`  ${v.name} ${v.version}: ${v.license}`)
  }
  process.exit(1)
}

// Write Markdown table
mkdirSync(join(ROOT, 'legal'), { recursive: true })

const lines = [
  '# Third-Party Rust Dependencies',
  '',
  '> Auto-generated by `npm run licence:rust`. Regenerate after Cargo.lock changes.',
  `> Last updated: ${new Date().toISOString().slice(0, 10)}`,
  '',
  '| Crate | Version | Licence (SPDX) | Repository |',
  '|-------|---------|----------------|------------|',
  ...deps.map((d) => `| ${d.name} | ${d.version} | ${d.license} | ${d.repository} |`),
]

writeFileSync(join(ROOT, 'legal', 'rust-dependency-licences.md'), lines.join('\n') + '\n')

console.log(`✓ Wrote legal/rust-dependency-licences.md (${deps.length} crates, 0 violations)`)
