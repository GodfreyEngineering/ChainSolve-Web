#!/usr/bin/env node
/**
 * generate-licenses.mjs — L0-1: Generate THIRD_PARTY_NOTICES.md.
 *
 * Collects license information from:
 *   1. npm production dependencies (reads node_modules/<pkg>/package.json)
 *   2. Cargo workspace dependencies (via `cargo metadata`)
 *   3. Manually tracked assets (fonts, icons)
 *
 * Usage:
 *   node scripts/generate-licenses.mjs          # generate THIRD_PARTY_NOTICES.md
 *   node scripts/generate-licenses.mjs --check  # verify committed file is up-to-date
 *
 * Forbidden licenses (for a commercial product): GPL-2.0, GPL-3.0, AGPL-*,
 * SSPL-*, EUPL-*, CC-BY-SA-*, CC-BY-NC-*.
 */

import { readFileSync, writeFileSync, existsSync, readdirSync, statSync } from 'node:fs'
import { join, resolve } from 'node:path'
import { execSync } from 'node:child_process'

const ROOT = resolve(import.meta.dirname, '..')
const OUT = join(ROOT, 'THIRD_PARTY_NOTICES.md')

// ── Forbidden license patterns (case-insensitive substring match) ───────────

const FORBIDDEN = [
  'GPL-2.0',
  'GPL-3.0',
  'AGPL-',
  'SSPL-',
  'EUPL-',
  'CC-BY-SA-',
  'CC-BY-NC-',
]

function isForbidden(license) {
  if (!license) return false
  const upper = license.toUpperCase()
  return FORBIDDEN.some((f) => upper.includes(f.toUpperCase()))
}

// ── npm dependencies ────────────────────────────────────────────────────────

function collectNpmDeps() {
  const pkgJsonPath = join(ROOT, 'package.json')
  const pkg = JSON.parse(readFileSync(pkgJsonPath, 'utf8'))
  const prodDeps = Object.keys(pkg.dependencies || {})

  const results = []
  for (const name of prodDeps.sort()) {
    const pkgDir = join(ROOT, 'node_modules', name)
    const subPkg = join(pkgDir, 'package.json')
    if (!existsSync(subPkg)) continue

    const meta = JSON.parse(readFileSync(subPkg, 'utf8'))
    const license = meta.license || 'Unknown'
    const version = meta.version || '?'
    const homepage = meta.homepage || meta.repository?.url || ''

    // Try to find a LICENSE file
    let licenseFile = null
    if (existsSync(pkgDir)) {
      const files = readdirSync(pkgDir)
      const match = files.find(
        (f) => /^licen[cs]e/i.test(f) || /^copying/i.test(f) || /^notice/i.test(f),
      )
      if (match) licenseFile = match
    }

    results.push({ name, version, license, homepage, licenseFile, source: 'npm' })
  }
  return results
}

// ── Cargo dependencies ──────────────────────────────────────────────────────

function collectCargoDeps() {
  let json
  try {
    json = execSync('cargo metadata --format-version 1', {
      cwd: ROOT,
      encoding: 'utf8',
      stdio: ['pipe', 'pipe', 'pipe'],
    })
  } catch {
    console.warn('WARN: cargo metadata failed; skipping Rust deps')
    return []
  }

  const meta = JSON.parse(json)
  const wsMembers = new Set(meta.workspace_members.map((m) => m.split('#')[0]))

  const results = []
  for (const pkg of meta.packages) {
    // Skip workspace-local crates
    if (wsMembers.has(pkg.id.split('#')[0])) continue

    const license = pkg.license || 'Unknown'
    const version = pkg.version || '?'
    const homepage = pkg.repository || ''

    results.push({
      name: pkg.name,
      version,
      license,
      homepage,
      licenseFile: null,
      source: 'cargo',
    })
  }

  // Sort and deduplicate (same name+version)
  results.sort((a, b) => a.name.localeCompare(b.name) || a.version.localeCompare(b.version))
  return results
}

// ── Manual asset inventory ──────────────────────────────────────────────────

function getAssetEntries() {
  return [
    {
      name: 'Montserrat',
      version: 'variable',
      license: 'OFL-1.1',
      homepage: 'https://fonts.google.com/specimen/Montserrat',
      licenseFile: null,
      source: 'font',
      note: 'Loaded from Google Fonts CDN. SIL Open Font License.',
    },
    {
      name: 'JetBrains Mono',
      version: 'variable',
      license: 'OFL-1.1',
      homepage: 'https://fonts.google.com/specimen/JetBrains+Mono',
      licenseFile: null,
      source: 'font',
      note: 'Loaded from Google Fonts CDN. SIL Open Font License.',
    },
  ]
}

// ── Markdown generation ─────────────────────────────────────────────────────

function generateMarkdown(npm, cargo, assets) {
  const now = new Date().toISOString().slice(0, 10)
  const lines = [
    '# Third-Party Notices',
    '',
    `> Auto-generated by \`scripts/generate-licenses.mjs\` on ${now}.`,
    '> To regenerate: `node scripts/generate-licenses.mjs`',
    '',
    'This file lists the third-party software and assets used by ChainSolve,',
    'along with their license types. No third-party code is modified; all',
    'dependencies are used as distributed by their authors.',
    '',
  ]

  // npm section
  lines.push('## npm dependencies (production)')
  lines.push('')
  lines.push('| Package | Version | License | Homepage |')
  lines.push('|---------|---------|---------|----------|')
  for (const dep of npm) {
    const link = dep.homepage ? `[link](${cleanUrl(dep.homepage)})` : ''
    lines.push(`| ${dep.name} | ${dep.version} | ${dep.license} | ${link} |`)
  }
  lines.push('')

  // Cargo section
  lines.push('## Cargo dependencies (Rust)')
  lines.push('')
  lines.push('| Crate | Version | License |')
  lines.push('|-------|---------|---------|')
  for (const dep of cargo) {
    lines.push(`| ${dep.name} | ${dep.version} | ${dep.license} |`)
  }
  lines.push('')

  // Assets section
  lines.push('## Fonts and assets')
  lines.push('')
  lines.push('| Asset | License | Notes |')
  lines.push('|-------|---------|-------|')
  for (const a of assets) {
    lines.push(`| ${a.name} | ${a.license} | ${a.note || ''} |`)
  }
  lines.push('')

  // Summary
  const total = npm.length + cargo.length + assets.length
  lines.push(`---`)
  lines.push('')
  lines.push(`Total: ${npm.length} npm + ${cargo.length} cargo + ${assets.length} asset entries = ${total} items.`)
  lines.push('')

  return lines.join('\n')
}

function cleanUrl(url) {
  return url
    .replace(/^git\+/, '')
    .replace(/\.git$/, '')
    .replace(/^ssh:\/\/git@/, 'https://')
}

// ── Main ────────────────────────────────────────────────────────────────────

const isCheck = process.argv.includes('--check')

console.log('Collecting npm production dependencies...')
const npmDeps = collectNpmDeps()
console.log(`  Found ${npmDeps.length} npm dependencies.`)

console.log('Collecting Cargo dependencies...')
const cargoDeps = collectCargoDeps()
console.log(`  Found ${cargoDeps.length} Cargo dependencies.`)

const assets = getAssetEntries()
console.log(`  Found ${assets.length} asset entries.`)

// Check for forbidden licenses
const allDeps = [...npmDeps, ...cargoDeps, ...assets]
const forbidden = allDeps.filter((d) => isForbidden(d.license))
if (forbidden.length > 0) {
  console.error('\nERROR: Forbidden licenses detected:')
  for (const d of forbidden) {
    console.error(`  ${d.source}/${d.name}@${d.version}: ${d.license}`)
  }
  process.exit(1)
}

const markdown = generateMarkdown(npmDeps, cargoDeps, assets)

if (isCheck) {
  // Check mode: verify committed file matches
  if (!existsSync(OUT)) {
    console.error('ERROR: THIRD_PARTY_NOTICES.md does not exist.')
    console.error('Run: node scripts/generate-licenses.mjs')
    process.exit(1)
  }

  const existing = readFileSync(OUT, 'utf8')

  // Compare ignoring the date line (which changes every run)
  const normalize = (s) =>
    s
      .split('\n')
      .filter((l) => !l.startsWith('> Auto-generated by'))
      .join('\n')
      .trim()

  if (normalize(existing) !== normalize(markdown)) {
    console.error('ERROR: THIRD_PARTY_NOTICES.md is out of date.')
    console.error('Run: node scripts/generate-licenses.mjs')
    process.exit(1)
  }

  console.log('THIRD_PARTY_NOTICES.md is up to date.')
} else {
  writeFileSync(OUT, markdown, 'utf8')
  console.log(`\nWrote ${OUT}`)
  console.log(`  ${npmDeps.length} npm + ${cargoDeps.length} cargo + ${assets.length} assets = ${allDeps.length} total entries.`)
  console.log('  No forbidden licenses detected.')
}
